package game; // 이 클래스가 속한 패키지를 선언합니다.

import java.util.Scanner; // 사용자로부터 키보드 입력을 받기 위한 도구(Scanner)를 가져옵니다.
import java.util.Random; // 정답을 무작위(랜덤)로 생성하기 위한 도구(Random)를 가져옵니다.

public class UpAndDown2_2 { // UpAndDown 이라는 이름의 메인 클래스를 선언합니다.
    public static void main(String[] args) { // 자바 프로그램이 실행될 때 가장 먼저 시작되는 메인 메서드입니다.
        
        // 1. 키보드 생성 및 랜덤 도구 생성
        Scanner 키보드 = new Scanner(System.in); // 콘솔창에서 사용자의 키보드 입력을 읽어올 스캐너 객체를 생성합니다.
        Random 랜덤 = new Random(); // 1~100 사이의 임의의 숫자를 뽑아낼 랜덤 객체를 생성합니다.
        
        // 2. 초기 변수 세팅
        // 정답을 랜덤으로 설정 (1~100 사이의 숫자)
        int 정답 = 랜덤.nextInt(100) + 1; // 0~99까지의 난수에 +1을 하여 1부터 100까지의 랜덤 숫자를 '정답' 변수에 저장합니다.
        int 점수 = 100; // 게임 시작 점수인 100점을 '점수' 변수에 할당합니다.
        
        System.out.println("=== Up & Down 게임 시작! (1~100) ==="); // 게임 시작을 알리는 안내 문구를 출력합니다.
        System.out.println("기본 점수는 100점이며, 틀릴 때마다 10점씩 차감됩니다."); // 룰을 설명하는 문구를 출력합니다.

        // 3. 무한 반복을 돌려서 입력한 숫자와 정답을 맞추는 게임 진행
        while (true) { // 정답을 맞추거나 점수가 바닥날 때까지 계속 반복하는 무한 루프입니다.
            
            // 점수가 0 미만으로 떨어지면 게임 종료! (0점일 때 틀리면 -10이 되므로 0 미만으로 체크)
            if (점수 < 0) { // 만약 점수가 0보다 작아졌다면 (패배 조건)
                System.out.println("점수가 0미만으로 떨어졌습니다. 게임 오버!"); // 실패 메시지를 출력합니다.
                System.out.println("정답은 " + 정답 + "였습니다."); // 정답을 공개합니다.
                break; // while 무한 루프를 강제로 탈출하여 프로그램을 종료합니다.
            }

            System.out.print("\n숫자를 입력하세요! (현재 점수: " + 점수 + ") : "); // 줄바꿈(\n)과 함께 입력을 유도하는 문구를 출력합니다.
            int 입력 = 키보드.nextInt(); // 사용자가 입력한 정수를 '입력' 변수에 저장합니다.
            
            // 입력값과 정답 비교 로직
            if (입력 == 정답) { // 사용자가 입력한 숫자와 랜덤으로 생성된 정답이 정확히 일치한다면
                System.out.println("정답입니다! 게임 종료."); // 정답 축하 메시지를 출력합니다.
                System.out.println("최종 점수: " + 점수 + "점"); // 차감되고 남은 최종 점수를 출력합니다.
                break; // 정답을 맞췄으므로 while 무한 루프를 탈출합니다.
                
            } else if (입력 > 정답) { // 사용자가 입력한 숫자가 정답보다 크다면
                // [수정 반영] 틀렸다는 명확한 문구와 함께 아래로 가라는 힌트 출력
                System.out.println("틀렸습니다! 정답은 더 아래(Down)에 있습니다."); 
                점수 = 점수 - 10; // 오답이므로 현재 점수에서 10점을 빼서 다시 점수 변수에 덮어씁니다.
                
            } else if (입력 < 정답) { // 사용자가 입력한 숫자가 정답보다 작다면
                // [수정 반영] 틀렸다는 명확한 문구와 함께 위로 가라는 힌트 출력
                System.out.println("틀렸습니다! 정답은 더 위(Up)에 있습니다."); 
                점수 = 점수 - 10; // 오답이므로 현재 점수에서 10점을 차감합니다.
            }
        } // while 루프의 끝. break를 만나지 않았다면 다시 위로 올라가 입력을 받습니다.
        
        // 키보드 자원 반납 (실무 권장 사항)
        키보드.close(); // 더 이상 스캐너를 사용하지 않으므로 메모리 누수를 막기 위해 닫아줍니다.
    }
}